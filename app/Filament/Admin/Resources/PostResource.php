    public static function generateSeoTitle(string $title): string
    {
        // Gi·ªõi h·∫°n ƒë·ªô d√†i SEO title trong kho·∫£ng 50-60 k√Ω t·ª±
        $maxLength = 60;

        if (strlen($title) <= $maxLength) {
