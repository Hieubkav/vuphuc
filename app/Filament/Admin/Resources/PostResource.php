        // C·∫Øt ng·∫Øn t·∫°i t·ª´ cu·ªëi c√πng ƒë·ªÉ tr√°nh c·∫Øt gi·ªØa t·ª´
        $truncated = substr($title, 0, $maxLength - 3);
